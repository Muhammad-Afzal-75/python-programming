# Variable Declaration and Initialization
# Basic data types in Python
name = "Afzal"
age = 25
height = 5.9
is_student = True
print(name, age, height, is_student)

# data types in Python
# int - Integer
# whole numbers without decimal points
num1 = 10
num2 = -5   
print(num1, num2)
print(type(num1), type(num2))

# float - Floating Point Number
# numbers with decimal points
pi = 3.14
gravity = 9.81
print(pi, gravity)
print(type(pi), type(gravity))

# str - String
# any sequence of characters enclosed in single or double quotes
greeting = "Hello, World!"
language = 'Python'
print(greeting, language)
print(type(greeting), type(language))

# bool - Boolean
# represents two values: True or False
is_raining = False
is_sunny = True
print(is_raining, is_sunny)
print(type(is_raining), type(is_sunny)) 

# list - List
# ordered collection of items, can contain mixed data types
fruits = ["apple", "banana", "cherry"]
numbers = [1, 2, 3, 4, 5]
print(fruits, numbers)
print(type(fruits), type(numbers))

# touple - Tuple
# ordered collection of items, immutable (cannot be changed)
coordinates = (10.0, 20.0)
colors = ("red", "green", "blue")
print(coordinates, colors)
print(type(coordinates), type(colors))

# dict - Dictionary
# collection of key-value pairs
person = {"name": "Alice", "age": 30}
car = {"make": "Toyota", "model": "Camry"}
print(person, car)
print(type(person), type(car))

# set - Set
# unordered collection of unique items
unique_numbers = {1, 2, 3, 4, 5}
unique_fruits = {"apple", "banana", "cherry"}
print(unique_numbers, unique_fruits)
print(type(unique_numbers), type(unique_fruits))

# NoneType - None
# represents the absence of a value
nothing = None
print(nothing)
print(type(nothing))
# End of basic data types in Python
