num1 = 10
num2 = 3

# Addition
sum_result = num1 + num2
print("Addition:", sum_result)
# Subtraction
sub_result = num1 - num2
print("Subtraction:", sub_result)
# Multiplication
mul_result = num1 * num2
print("Multiplication:", mul_result)
# Division  
div_result = num1 / num2
print("Division:", div_result)  # returns a float
# Floor Division
floor_div_result = num1 // num2
print("Floor Division:", floor_div_result)  # returns an integer
# Modulus
mod_result = num1 % num2
print("Modulus:", mod_result)
# Exponentiation
exp_result = num1 ** num2
print("Exponentiation:", exp_result)
# Comparison Operators
print("Equal:", num1 == num2)
print("Not Equal:", num1 != num2)
print("Greater Than:", num1 > num2)
print("Less Than:", num1 < num2)
print("Greater Than or Equal To:", num1 >= num2)
print("Less Than or Equal To:", num1 <= num2)
# Logical Operators
and_result = (num1 > 5) and (num2 < 5)
print("Logical AND:", and_result)

or_result = (num1 > 5) or (num2 > 5)
print("Logical OR:", or_result)
not_result = not(num1 > 5)
print("Logical NOT:", not_result)
# Bitwise Operators
bit_and_result = num1 & num2
print("Bitwise AND:", bit_and_result)
bit_or_result = num1 | num2
print("Bitwise OR:", bit_or_result)
bit_xor_result = num1 ^ num2
print("Bitwise XOR:", bit_xor_result)
bit_not_result = ~num1
print("Bitwise NOT:", bit_not_result)
bit_left_shift_result = num1 << 1
print("Bitwise Left Shift:", bit_left_shift_result)
bit_right_shift_result = num1 >> 1
print("Bitwise Right Shift:", bit_right_shift_result)
bit_right_shift_result = num1 >> 1
print("Bitwise Right Shift:", bit_right_shift_result)
# Membership Operators
sample_list = [10, 20, 30, 40, 50]
in_result = num1 in sample_list
print("Membership IN:", in_result)
not_in_result = num2 not in sample_list
print("Membership NOT IN:", not_in_result)
# Identity Operators
a = 100
b = 100
is_result = a is b
print("Identity IS:", is_result)
is_not_result = a is not b
print("Identity IS NOT:", is_not_result)
