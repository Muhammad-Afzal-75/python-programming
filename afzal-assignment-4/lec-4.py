# LEC 4: Data Types, Variables & Operators

# 1. Integer Operations
a = 10
b = 3

# Addition
sum_result = a + b
print("Addition:", sum_result)  # Output: 13
# Subtraction
sub_result = a - b
print("Subtraction:", sub_result)  # Output: 7
# Multiplication
mul_result = a * b  
print("Multiplication:", mul_result)  # Output: 30
# Division
div_result = a / b
print("Division:", div_result)  # Output: 3.3333333333333335
# Floor Division
floor_div_result = a // b
print("Floor Division:", floor_div_result)  # Output: 3
# Modulus
mod_result = a % b
print("Modulus:", mod_result)  # Output: 1
# Exponentiation
exp_result = a ** b
print("Exponentiation:", exp_result)  # Output: 1000
# 2. Float Operations
x = 5.5
y = 2.0
# Addition
float_sum = x + y

print("Float Addition:", float_sum)  # Output: 7.5
# Subtraction   
float_sub = x - y
print("Float Subtraction:", float_sub)  # Output: 3.5
# Multiplication
float_mul = x * y
print("Float Multiplication:", float_mul)  # Output: 11.0
# Division
float_div = x / y
print("Float Division:", float_div)  # Output: 2.75
# 3. String Operations
str1 = "Hello"
str2 = "World"
# Concatenation
concat_str = str1 + " " + str2
print("String Concatenation:", concat_str)  # Output: Hello World
# Repetition
repeat_str = str1 * 3
print("String Repetition:", repeat_str)  # Output: HelloHelloHello
